import pytest

INPUT_TXT = os.path.join(os.path.dirname(__file__), "input.txt")


CYCLES = {20, 60, 100, 140, 180, 220}


def compute(input: str) -> int:
    cycle_counter = 0
    instruction_cycles = 0
    sum_ = 0
    register = 1
    for line in input.splitlines():
        if line == "noop":
            instruction_cycles = 1
        else:
            instruction_cycles = 2

        for i in reversed(range(instruction_cycles)):

            cycle_counter += 1

            if cycle_counter in CYCLES:
                sum_ += register * cycle_counter

            if i == 0:
                if line.startswith("addx"):
                    _, add = line.split()
                    register += int(add)

        if instruction_cycles > max(CYCLES):
            break

    return sum_


